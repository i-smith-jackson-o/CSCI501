// This program determines the size of integers, long integers
// and long doubles. 
#include <iostream>
using namespace std; 

int main() {
  long double apple; 

  cout << "int " << sizeof(int) << " bytes\n"; 
  cout << "long int" << sizeof(long) << " bytes\n"; 
  cout <<"long double" << sizeof(apple) << " bytes"; 

  return 0; 
}










